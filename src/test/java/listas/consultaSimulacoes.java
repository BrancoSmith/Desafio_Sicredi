package listas;

public class consultaSimulacoes {
	private Object parcelas;
	private Object seguro;
	private Object cpf;
	private Object valor;
	private Object nome;
	private Object id;
	private Object email;
	private Object message;

	public Object getParcelas() {
		return parcelas;
	}

	public void setParcelas(Object parcelas) {
		this.parcelas = parcelas;
	}

	public Object getSeguro() {
		return seguro;

	}

	public void setSeguro(Object seguro) {
		this.seguro = seguro;
	}

	public Object getCpf() {
		return cpf;

	}

	public void setCpf(Object cpf) {
		this.cpf = cpf;
	}

	public Object getValor() {
		return valor;

	}

	public void setValor(Object valor) {
		this.valor = valor;
	}

	public Object getNome() {
		return nome;

	}

	public void setNome(Object nome) {
		this.nome = nome;
	}

	public Object getId() {
		return id;
	}

	public void setId(Object id) {
		this.id = id;
	}

	public Object getEmail() {
		return email;
	}

	public void setEmail(Object email) {
		this.email = email;
	}
	public Object getMessage() {
		return message;
	}
	
	public void setMessage(Object message) {
		this.message = message;
	}

}
